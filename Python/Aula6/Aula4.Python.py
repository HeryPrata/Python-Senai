#len() - Metodo que serve para contagem de caracteres, numeros, letras, palavras...
numeros = [10,20,30]
print("Quantidade de número: ", len(numeros))#len = ver a quantidade de numeros dentro da lista

#sum() - Metodo que serve para somar os elementos númericos
notas = [7.5, 8.5, 10]
print(f"Soma das notas é: {sum(notas)}")
print(f"A média de notas é: {sum(notas)/len(notas):.2f}")
